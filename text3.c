#include<stdio.h>

int main(){
	float x=0,y=0;
	float d1,d2,d3,d4;
	printf("请输入坐标如（2,2）\n");
	printf("请确保在英文输入法下输入括号和逗号\n"); 
	scanf("(%f,%f)",&x,&y);
	d1=(x-2.0)*(x-2.0)+(y-2.0)*(y-2.0);	 
	d2=(x+2.0)*(x+2.0)+(y-2.0)*(y-2.0);	 
	d3=(x-2.0)*(x-2.0)+(y+2.0)*(y+2.0);	 
	d4=(x+2.0)*(x+2.0)+(y+2.0)*(y+2.0);	 
	if(d1<=1||d2<=1||d3<=1||d4<=1){
		printf("此点的高度为10米");
	} 
	else{
		printf("此点的高度为0米"); 
	} 
	return 0;
} 
